    static public void mostraErroriSwing(java.awt.Component thrower,SQLException e) {
      String msg;
      msg = e.getMessage() + "\n";
      msg += "SQLState= " + e.getSQLState() + "\n";
                                                  
      JOptionPane.showMessageDialog(thrower, msg, "Errore " + e.getErrorCode(),       
              JOptionPane.ERROR_MESSAGE);       
   }
    
    
    
    
    
    
    
    
    
